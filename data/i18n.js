function t(key, vars = {}) {
  const lang = (typeof SaveSystem !== 'undefined' ? SaveSystem.load('lang') : null) || 'zh';
  let str = I18N[lang]?.[key] ?? I18N.zh?.[key] ?? key;
  Object.entries(vars).forEach(([k, v]) => { str = str.replace(`{{${k}}}`, String(v)); });
  return str;
}

function getLang() {
  return (typeof SaveSystem !== 'undefined' ? SaveSystem.load('lang') : null) || 'zh';
}

function setLang(lang) {
  if (typeof SaveSystem !== 'undefined') SaveSystem.save('lang', lang);
}
